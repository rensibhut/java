class math
{
    public static void main(String s[])
    {
        System.out.println("Math Functions");
        System.out.println("Absolute Value of -5 :"+Math.abs(-5));
        System.out.println("Max Value (20,25) :"+Math.max(20,25));
        System.out.println("Min Value (20,25) :"+Math.min(20,25));
        System.out.println("Return Power :"+Math.pow(2,5));
        System.out.println("Sqrt :"+Math.sqrt(625));
    }
}


